
 CREATE TABLE USER_TABLE  ( 
  USER_ID NUMBER(5),
  NICKNAME VARCHAR2(25),
  FIRSTNAME VARCHAR2(40),
  LASTNAME VARCHAR2(40),
  LOCATION VARCHAR2(100),
  EMAIL VARCHAR2(50),
  DATE_OF_BIRTH DATE ,
 
  CONSTRAINT USER_TABLE_PK PRIMARY KEY(USER_ID)
 );
  


 CREATE TABLE TWEETS (
  
  TWEET_ID NUMBER(10),
  TWEET_TEXT VARCHAR2(500),
  LOCATION VARCHAR2(50),
  TWEET_SENT_TIME TIMESTAMP,
  AUTHOR_ID NUMBER(5) ,
  REPOST_IND NUMBER(1) DEFAULT 0,
  PARENT_TWEET_ID NUMBER(10),
  
  CONSTRAINT TWEETS_PK PRIMARY KEY(TWEET_ID),
  CONSTRAINT TWEETS_FK_1 FOREIGN KEY(AUTHOR_ID) REFERENCES USER_TABLE(USER_ID),
  CONSTRAINT TWEETS_FK_2 FOREIGN KEY(PARENT_TWEET_ID) REFERENCES TWEETS(TWEET_ID)

 );

 
 CREATE TABLE HASHTAGS (
  
  TWEET_ID NUMBER(10),
  HASHTAG VARCHAR2(100),
   
  CONSTRAINT HASHTAGS_PK PRIMARY KEY(TWEET_ID, HASHTAG ),
  CONSTRAINT HASHTAGS_FK FOREIGN KEY(TWEET_ID) REFERENCES TWEETS(TWEET_ID)

 );


 
 CREATE TABLE FOLLOWS (
  
  USER_ID NUMBER(5),
  FOLLOWS_ID NUMBER(5), 
   
  CONSTRAINT FOLLOWS_PK PRIMARY KEY(USER_ID, FOLLOWS_ID ) ,
  CONSTRAINT FOLLOWS_FK_1 FOREIGN KEY(USER_ID) REFERENCES USER_TABLE(USER_ID) ,
  CONSTRAINT FOLLOWS_FK_2 FOREIGN KEY(FOLLOWS_ID) REFERENCES USER_TABLE(USER_ID)

 
 );

 
 CREATE TABLE PRIVATE_MESSAGE (
  
  SENDER_ID NUMBER(5),
  RECEIVER_ID NUMBER(5),
  TIME_SENT DATE, 
  MESSAGE_CONTENT VARCHAR2(250),
  
  CONSTRAINT PRIVATE_MESSAGE_PK PRIMARY KEY(SENDER_ID, RECEIVER_ID, TIME_SENT ) ,
  CONSTRAINT PRIVATE_MESSAGE_FK1 FOREIGN KEY(SENDER_ID) REFERENCES  USER_TABLE(USER_ID) ,
  CONSTRAINT PRIVATE_MESSAGE_FK2 FOREIGN KEY(RECEIVER_ID) REFERENCES  USER_TABLE(USER_ID)
 
 );


